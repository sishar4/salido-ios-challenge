#import "SWShoppingCart.h"

@implementation SWShoppingCart

+ (id)sharedInstance
{
    static SWShoppingCart *sharedInstance = nil;
    static dispatch_once_t onceToken;
    dispatch_once(&onceToken, ^{
        sharedInstance = [[self alloc] init];
    });
    
    return sharedInstance;
}

@end
